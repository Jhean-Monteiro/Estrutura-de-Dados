// Declare um vetor de 5 inteiros e atribua valores manualmente.

#include <stdio.h>

int main() {
    int vetor[5];

    vetor[0] = 10;
    vetor[1] = 25;
    vetor[2] = -7;
    vetor[3] = 42;
    vetor[4] = 8;

    printf("Vetor com valores manuais %d %d %d %d %d\n", vetor[0], vetor[1], vetor[2], vetor[3], vetor[4]);

    return 0;
}